from PIL import ImageFilter
import numpy as np


class GaussianBlur(object):
    def __init__(self, p=1):
        self.p = p

    def __call__(self, img):
        if np.random.rand() < self.p:
            sigma = np.random.rand() * 1.9 + 0.1
            return img.filter(ImageFilter.GaussianBlur(sigma))
        else:
            return img
